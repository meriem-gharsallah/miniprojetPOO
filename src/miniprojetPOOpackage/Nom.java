package miniprojetPOOpackage;

public class Nom {
    private int id;
    private String texte;

    public Nom(int id, String texte) {
        this.id = id;
        this.texte = texte;
    }

    public int getId() {
        return id;
    }

    public String getTexte() {
        return texte;
    }
}
